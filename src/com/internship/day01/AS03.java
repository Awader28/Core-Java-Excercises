package com.internship.day01;

public class AS03 {

	static int cId;
	static String cName; //must need not to create a class object if the variables are static
	static short s1;
	static double d;
	static float f;
	static byte b;
	static boolean bu;
	   
	
	public static void main(String[] args) {
		System.out.println("integer= "+cId);
		System.out.println("String = "+cName);
		System.out.println("Short= "+s1);
		System.out.println("Double = "+d);
		System.out.println("Float= "+f);
		System.out.println("Byte "+b);
		System.out.println("Boolean "+bu);
		System.out.println("\n");
		
				
		
	}
	

}
